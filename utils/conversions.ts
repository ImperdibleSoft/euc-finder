export const inchesToCentimeters = (inches: number) => inches * 2.54;

export const kilometersToMiles = (kilometers: number) => kilometers * 0.621371;

export const kilogramsToPounds = (kilograms: number) => kilograms * 2.20462;

export const milimetersToInches = (milimeters: number) => milimeters * 0.0393701;